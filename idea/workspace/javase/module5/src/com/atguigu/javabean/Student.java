package com.atguigu.javabean;

public class Student {
    private int sid;
    private String name;

    // 快捷键生成 javabean alt + insert
    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

